#import "Timer.h"
//------------------------------------------------------------------------------
@interface Timer ()
{
    NSDate* _start;
    BOOL _hasStarted;
}
@end
//------------------------------------------------------------------------------
@implementation Timer
//------------------------------------------------------------------------------
-(id)init
{
    self = [super init];
    
    _start = nil;
    _hasStarted = NO;
    
    return self;
}
//------------------------------------------------------------------------------
-(void)dealloc
{

}
//------------------------------------------------------------------------------
-(void)start
{
    if (_hasStarted)
    {
        return;
    }
    
    _start = [NSDate date];
}
//------------------------------------------------------------------------------
-(void)stop
{
    _hasStarted = NO;
}
//------------------------------------------------------------------------------
-(void)reset
{
    [self stop];
    [self start];
}
//------------------------------------------------------------------------------
-(double)getElapsed
{
    return [[NSDate date] timeIntervalSinceDate:_start];
}
//------------------------------------------------------------------------------
@end
//------------------------------------------------------------------------------